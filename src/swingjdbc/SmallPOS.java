    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDeleteActionPerformed
    {//GEN-HEADEREND:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int index = this.tblItem.getSelectedRow();
        if(index == -1)
        {
            JOptionPane.showMessageDialog(null, "Please select an item to delete");
        }
        else
        {
             int result = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete ", "Delete", JOptionPane.OK_CANCEL_OPTION);
             if(result == JOptionPane.OK_OPTION)
             {
                 ((DefaultTableModel)tblItem.getModel()).removeRow(index);
             }
            
            
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

