    private void btnAddActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAddActionPerformed
    {//GEN-HEADEREND:event_btnAddActionPerformed
        // TODO add your handling code here:
        this.dlgNewItem.setVisible(false);
        
        DefaultTableModel model = (DefaultTableModel)this.tblItem.getModel();
        
        String name = this.txtName.getText();
        String price = this.txtPrice.getText();
        String[] row = new String[]{"1",name, price };
        model.addRow(row);
        
        this.txtName.setText("");
        this.txtPrice.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditItemActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnEditItemActionPerformed
    {//GEN-HEADEREND:event_btnEditItemActionPerformed
        // TODO add your handling code here:
        int index = this.tblItem.getSelectedRow();
        if(index == -1)
        {
            JOptionPane.showMessageDialog(null, "Please select an item to edit");
        }
        else
        {
            this.editRow = index;
            String name = (String)this.tblItem.getValueAt(index, 1);
            String price = (String)this.tblItem.getValueAt(index, 2);
             
            this.txtEditName.setText(name);
            this.txtEditPrice.setText(price);
             
            this.dlgEdit.setModal(true);
            this.dlgEdit.setVisible(true);
            
            
        }
    }//GEN-LAST:event_btnEditItemActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnEditActionPerformed
    {//GEN-HEADEREND:event_btnEditActionPerformed
        editAction();
       
       
    }//GEN-LAST:event_btnEditActionPerformed

    private void editAction()
    {
        // TODO add your handling code here:
        System.out.println("Selected Row "+ this.editRow);
        String name = this.txtEditName.getText();
        String price = this.txtEditPrice.getText();
        
        
        
        this.tblItem.setValueAt(name, this.editRow, 1);
        this.tblItem.setValueAt(price, this.editRow, 2);
        
        this.dlgEdit.setVisible(false);
    }

